    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        // TODO add your handling code here:
        //Chọn hàng cần xóa
        int hang=tabnv.getSelectedRow();
        //Hỏi xác nhận xóa
        int kq=JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xóa", "Xác nhận", JOptionPane.WARNING_MESSAGE);
        if(kq==JOptionPane.YES_NO_OPTION)
        //Xóa khỏi List
        lstNV.remove(hang);
        //Cập nhật
        //1.Tạo mới để setModel
        DefaultTableModel model = new DefaultTableModel();
  
        //1.2.Đặt tên cho các cột
        model.addColumn("Mã nhân viên");
        model.addColumn("Phân xưởng");
        model.addColumn("số sản phẩm");
        model.addColumn("Vượt chuẩn");
        //2.Duyệt ArrayList để đưa vào Model
        for(int i=0; i<lstNV.size();i++)
        {
            Nhanvien NV = lstNV.get(i);
            Vector<String> h=new Vector<>();
            h.add(NV.getManv());
            h.add(NV.getPhanxuong());
            h.add(Integer.toString(NV.getSosp()));
            String c;
            if(NV.VuotChuan()==true)
                c = "X";
            else
                c = "";
            h.add(c);
            model.addRow(h);
        }
        //3.Hiện ra điều khiển
        tabnv.setModel(model);
    }//GEN-LAST:event_btnxoaActionPerformed

    private void tabnvMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabnvMouseClicked
        // TODO add your handling code here:
        //lay ve model
        TableModel mData = tabnv.getModel();
        //lay dong dk chon
        int chon = tabnv.getSelectedRow();
        txtmanv.setText(mData.getValueAt(chon, 0).toString());
        combophanxuong.setSelectedItem(mData.getValueAt(chon, 1).toString());
        txtsosp.setText(mData.getValueAt(chon, 2).toString());
    }//GEN-LAST:event_tabnvMouseClicked

