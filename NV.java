    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        // TODO add your handling code here:
        Nhanvien nv = new Nhanvien();
        try{
            manv = txtmanv.getText();
            sosp = Integer.parseInt(txtsosp.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"kiem tra lai thong tin");
        }
        if (txtmanv.getText().trim().equals("") || txtsosp.getText().trim().equals(""))
           
            JOptionPane.showMessageDialog(this, "Có dữ liệu chưa nhập");
        else{           
            
            nv.setManv(txtmanv.getText());
            nv.setPhanxuong(combophanxuong.getSelectedItem().toString()); }
        //b1: lay data tu các ctrl
        phanxuong = combophanxuong.getSelectedItem().toString();
        //b2 Dua vao list sv
        
        nv.setManv(manv);
        nv.setSosp(sosp);
        nv.setPhanxuong(phanxuong);
        nv.VuotChuan();
        //them vo ds
        lstNV.add(nv);
        //b3: hien len jtable
        //1. tao moi de setModel
        DefaultTableModel model = new DefaultTableModel();
        //1.2 dat ten cho cac cot
        model.addColumn("Mã nhân viên");
        model.addColumn("Phân xưởng");
        model.addColumn("số sản phẩm");
        model.addColumn("Vượt chuẩn");
        //2. duyet araylisst de dua vao model
        for(int i=0; i<lstNV.size();i++)
        {
            Nhanvien NV = lstNV.get(i);
            Vector<String> h = new Vector<>();
            h.add(NV.getManv());
            h.add(NV.getPhanxuong());
            h.add(Integer.toString(NV.getSosp()));
            String c;
            if(NV.VuotChuan()==true)
                c = "X";
            else
                c = "";
            h.add(c);
            model.addRow(h);
        }
        //3. hien ra dieu khien
        tabnv.setModel(model);
        
    }//GEN-LAST:event_btnthemActionPerformed

